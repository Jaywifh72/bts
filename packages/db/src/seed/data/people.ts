export async function seedPeople(db: SeedDb) {
  for (const p of peopleData) {
    await db.insert(people)
      .values({
        slug: p.slug,
        displayName: p.displayName,
        givenName: p.givenName ?? null,
        familyName: p.familyName ?? null,
        country: p.country ?? null,
        memberSocieties: p.memberSocieties ?? [],
        aliases: p.aliases ?? [],
        bio: p.bio ?? null,
        imdbId: p.imdbId ?? null,
        wikidataId: p.wikidataId ?? null,
      })
      .onConflictDoUpdate({
        target: people.slug,
        set: {
          displayName: p.displayName,
          givenName: p.givenName ?? null,
          familyName: p.familyName ?? null,
          country: p.country ?? null,
          memberSocieties: p.memberSocieties ?? [],
          aliases: p.aliases ?? [],
          bio: p.bio ?? null,
          updatedAt: new Date(),
        },
      });
  }
}
